package br.com.targettrust.traccadastros.config;

public class NotFoundRuntimeException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NotFoundRuntimeException() {
		super();
	}

	public NotFoundRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotFoundRuntimeException(String arg0) {
		super(arg0);
	}

	public NotFoundRuntimeException(Throwable arg0) {
		super(arg0);
	}

	
	
}
